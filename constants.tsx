export const CLEAN_IMG_URL = (url: string) => {
  // Fixes the array-in-string bug of the Platzi API
  try {
    const cleaned = url.replace(/[\[\]"]/g, "");
    if (cleaned.startsWith("http")) return cleaned;
    return `https://picsum.photos/seed/${Math.random()}/600/600`;
  } catch {
    return `https://picsum.photos/seed/${Math.random()}/600/600`;
  }
};
